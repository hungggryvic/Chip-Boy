static void drawLocationDataView() {
  display.clearDisplay();
  display.setTextColor(SSD1306_WHITE);
  display.setTextSize(1);

  display.setCursor(0, 0);
  display.println("ATGM336H GPS");

  display.setCursor(0, 12);
  display.print("Sats: ");
  if (gps.satellites.isValid()) display.print(gps.satellites.value());
  else display.print("--");

  display.setCursor(0, 22);
  display.print("Fix: ");
  display.print(gps.location.isValid() ? "YES" : "NO");

  if (gps.location.isValid()) {
    display.setCursor(0, 34);
    display.print("Lat:");
    display.setCursor(24, 34);
    display.print(gps.location.lat(), 6);
    display.setCursor(0, 46);
    display.print("Lon:");
    display.setCursor(24, 46);
    display.print(gps.location.lng(), 6);
  } else {
    display.setCursor(0, 40);
    display.print("Waiting for GPS.");
  }

  display.setCursor(0, 55);
  display.print("MAP");
  display.setCursor(104, 55);
  display.print("EXIT");
  display.display();
}

static void drawLocationMapView() {
  display.clearDisplay();
  display.drawBitmap(0, 0,
    epd_bitmap_6859d67ca9ff5c24497a5dd3_2036643898_7314_4,
    128, 64, SSD1306_WHITE);

  if (gps.location.isValid()) {
    int px, py;
    latLonToXY(gps.location.lat(), gps.location.lng(), px, py);
    drawCrosshairCircle(px, py, 4, SSD1306_INVERSE);
  }

  display.setTextSize(1);
  display.setTextColor(SSD1306_WHITE);
  display.setCursor(0, 55);
  display.print("DATA");
  display.setCursor(104, 55);
  display.print("EXIT");
  display.display();
}

// ── App lifecycle ─────────────────────────────────────────────────────────

void locationEnter() {
  inMenu = false;
  inLocation = true;
  uiDirty = true;
  gpsLastDrawMs = 0;
  locationMapMode = false;
  gps = TinyGPSPlus();
  GPSSerial.begin(GPS_BAUD, SERIAL_8N1, GPS_RX_PIN, GPS_TX_PIN);
}

void locationExit() {
  GPSSerial.end();
  inLocation = false;
  inMenu = true;
  uiDirty = true;
  lastSecondDrawn = 255;
  menuEnableButtonsAt = millis() + 120;
  redrawMenuNow();
}

void handleLocation() {
  if (btnWasShortPressed(b3)) { locationExit(); return; }

  if (btnWasShortPressed(b1)) {
    locationMapMode = !locationMapMode;
    uiDirty = true;
  }

  while (GPSSerial.available()) gps.encode(GPSSerial.read());

  uint32_t now = millis();
  if (!uiDirty && (now - gpsLastDrawMs) < 200) return;
  gpsLastDrawMs = now;
  uiDirty = false;

  if (!screenEnabled || !displayReady) return;

  if (locationMapMode) drawLocationMapView();
  else                drawLocationDataView();
}