// ── Helpers ───────────────────────────────────────────────────────────────

static void normalizeDateTimeSigned() {
  while (second < 0) { second += 60; minute--; }
  while (second >= 60) { second -= 60; minute++; }
  while (minute < 0) { minute += 60; hour--; }
  while (minute >= 60) { minute -= 60; hour++; }
  while (hour < 0) { hour += 24; day--; }
  while (hour >= 24) { hour -= 24; day++; }
  normalizeDateTime();
}

int getPacificOffsetMin(int y, int mon, int d, int h) {
  auto dowOf = [](int yr, int mo, int dy) -> int {
    if (mo < 3) { mo += 12; yr--; }
    int k = yr % 100, j = yr / 100;
    return (dy + (13*(mo+1)/5) + k + k/4 + j/4 + 5*j) % 7;
  };
  int dow1Mar = dowOf(y, 3, 1);
  int firstSunMar = (dow1Mar == 1) ? 1 : 1 + (8 - dow1Mar) % 7;
  int dstStart = firstSunMar + 7;
  int dow1Nov = dowOf(y, 11, 1);
  int dstEnd = (dow1Nov == 1) ? 1 : 1 + (8 - dow1Nov) % 7;
  bool isDST = false;
  if (mon > 3 && mon < 11) isDST = true;
  else if (mon == 3) isDST = (d > dstStart) || (d == dstStart && h >= 2);
  else if (mon == 11) isDST = (d < dstEnd) || (d == dstEnd && h < 2);
  return isDST ? -420 : -480;
}

bool gpsSyncClockOnBoot(uint32_t timeoutMs) {
  if (gpsBootTimeSet) return true;
  gps = TinyGPSPlus();
  GPSSerial.begin(GPS_BAUD, SERIAL_8N1, GPS_RX_PIN, GPS_TX_PIN);
  uint32_t start = millis();
  while (millis() - start < timeoutMs) {
    while (GPSSerial.available()) gps.encode(GPSSerial.read());
    if (gps.date.isValid() && gps.time.isValid() && gps.date.year() >= 2020) {
      year   = (int)gps.date.year();
      month  = (int)gps.date.month();
      day    = (int)gps.date.day();
      hour   = (int)gps.time.hour();
      minute = (int)gps.time.minute();
      second = (int)gps.time.second();
      int tzOffset = getPacificOffsetMin(year, month, day, hour);
      minute += tzOffset;
      normalizeDateTimeSigned();
      lastTickMs = millis();
      gpsBootTimeSet = true;
      uiDirty = true;
      lastSecondDrawn = 255;
      GPSSerial.end();
      return true;
    }
    delay(5);
    yield();
  }
  GPSSerial.end();
  return false;
}

// ── Map helpers ───────────────────────────────────────────────────────────

static inline int clampi(int v, int lo, int hi) {
  if (v < lo) return lo;
  if (v > hi) return hi;
  return v;
}

static void latLonToXY(double lat, double lon, int &x, int &y) {
  while (lon < -180.0) lon += 360.0;
  while (lon >= 180.0) lon -= 360.0;
  double xf = (lon + 180.0) * (127.0 / 360.0);
  double yf = (90.0 - lat)  * (63.0 / 180.0);
  x = clampi((int)(xf + 0.5), 0, 127);
  int yy = (int)(yf + 0.5) + MAP_Y_OFFSET;
  y = clampi(yy, 0, 63);
}

static void drawCrosshairCircle(int x, int y, int r, uint16_t color) {
  display.drawCircle(x, y, r, color);
  int x0 = clampi(x - (r + 2), 0, 127);
  int x1 = clampi(x + (r + 2), 0, 127);
  int y0 = clampi(y - (r + 2), 0, 63);
  int y1 = clampi(y + (r + 2), 0, 63);
  display.drawLine(x0, y, x1, y, color);
  display.drawLine(x, y0, x, y1, color);
  display.drawPixel(x, y, color);
}

